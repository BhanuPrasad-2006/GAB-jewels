  window.GAB_I18N = {
  
      current: localStorage.getItem('gab_lang') || 'en',
  
      get(key) {
          const lang = this.current;
          return (GAB_LANG[lang] && GAB_LANG[lang][key]) || GAB_LANG['en'][key] || key;
      },
  
      set(langCode) {
          if (!GAB_LANG[langCode]) return;
          this.current = langCode;
          localStorage.setItem('gab_lang', langCode);
          this.apply();
      },
  
      apply() {
          const lang = GAB_LANG[this.current];
          if (!lang) return;
  
          // Load native font if needed
          if (lang.font && !document.getElementById('gab-lang-font')) {
              const link = document.createElement('link');
              link.id   = 'gab-lang-font';
              link.rel  = 'stylesheet';
              link.href = lang.font;
              document.head.appendChild(link);
          }
  
          // Apply font to body for non-latin scripts
          if (this.current !== 'en') {
              const fontMap = {
                  hi: "'Noto Sans Devanagari', sans-serif",
                  ta: "'Noto Sans Tamil', sans-serif",
                  te: "'Noto Sans Telugu', sans-serif",
                  kn: "'Noto Sans Kannada', sans-serif",
                  ml: "'Noto Sans Malayalam', sans-serif",
              };
              document.body.style.fontFamily = fontMap[this.current] || '';
          } else {
              document.body.style.fontFamily = '';
          }
  
          // Translate all elements with data-i18n attribute
          document.querySelectorAll('[data-i18n]').forEach(el => {
              const key = el.getAttribute('data-i18n');
              const val = this.get(key);
              if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                  el.placeholder = val;
              } else {
                  el.textContent = val;
              }
          });
  
          // Translate placeholders
          document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
              el.placeholder = this.get(el.getAttribute('data-i18n-placeholder'));
          });
  
          // Update html lang attribute
          document.documentElement.lang = this.current;
          document.documentElement.dir  = lang.dir;
      },
  
      init() {
          this.apply();
      }
  };
  
